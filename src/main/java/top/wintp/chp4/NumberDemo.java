public class NumberDemo {

    public static void main(String[] args) {
        System.out.println("十进制直接书写数字即可:" + 10);
        System.out.println("二进制的数字使用0B开头:" + 0B10);
        System.out.println("八进制的数字使用数字0开头:" + 011);
        System.out.println("十六进制的数字使用0X开头:" + 0X11);

        /*
        输出结果为：
        十进制直接书写数字即可:10
        二进制的数字使用0B开头:2
        八进制的数字使用数字0开头:9
        十六进制的数字使用0X开头:17
         */

    }


}
